        <li>Generación de Contrato de Prestación de Servicios con el Punto de Disposición Final</li>
        <li>Verificación de Pagos</li>
        <li>Verificación por parte de la <b>“Autoridad Competente”</b> del cumplimiento de los planes de manejo de Residuos reportados por los centros autorizados de disposición final y <b>“El Usuario”</b>.</li>
